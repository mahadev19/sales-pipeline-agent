"""
agents/reviewer_agent.py
---------------------------
PURPOSE:
    The ReviewerAgent is a reflection and quality-control agent in the sales pipeline.
    It receives an email draft generated by the OutreachDrafter and evaluates it
    across 5 key dimensions: Personalization, Clarity, Tone, Call to Action, and Subject Line.

    If the draft score is below a threshold (35/50), it performs self-correction
    (reflection) by rewriting the email to improve it.

    It returns a structured review containing the original score, improved score,
    and the final email (either original or improved).

THE REFLECTION PATTERN:
    Reflection is a design pattern in agentic systems where an agent evaluates its
    own or another agent's output and uses that critique to refine the work.
    In multi-agent architectures, this acts as a quality gate.
    
    Why it improves output quality:
    1. Error Detection & Self-Correction: Large Language Models (LLMs) often make minor
       mistakes or generate generic prose on the first pass (zero-shot). By prompting
       the model to explicitly criticize and grade the output first, we activate its
       reasoning capabilities to detect weaknesses before returning the response.
    2. Decoupled Concerns: The generator agent (OutreachDrafter) focuses on styling and
       personalization, while the reviewer agent (ReviewerAgent) focuses on validation
       against specific criteria. This keeps agent prompts simpler and less prone to
       instruction drift.
    3. Iterative Refinement: Feedback loops allow the system to self-correct
       hallucinations or quality issues, leading to much more polished and higher-converting
       end-results.
"""

